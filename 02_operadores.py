# Operadores matemáticos

num1 = 15
num2 = 2

print("La suma de 11 y 2 es", num1 + num2)
print("La suma de " + str(num1) + " y " + str(num2) + " es " + str(num1 + num2))
print("La suma de", num1, "y", num2, "es", num1 + num2)
informacion = f"La suma de {num1} y {num2} es {num1 + num2}"
print(informacion)





print("La resta es",num1 - num2)
print("La multiplicación es",num1 * num2)
print("La división es",num1 / num2)

print("El módulo es",num1 % num2)
print("La división entera es", num1 // num2)
print("El exponente es",num1 ** num2)

# Operadores de comparación siempre retornan verdadero o falso
# operador de igualdad
print("El numero 1 es igual al número 2?", num1 == num2)
# operador de desigualdad
print("El numero 1 es igual al número 2?", num1 != num2)
